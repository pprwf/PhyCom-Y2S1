#include <stdio.h>

int main () {
    double d1, d2;
    scanf("%lf,%lf", &d1, &d2);
    printf("The sum of the given numbers : %lf\n", d1 + d2);
    printf("The difference of the given numbers : %lf\n", d1 - d2);
    printf("The product of the given numbers : %lf\n", d1 * d2);
    printf("The quotient of the given numbers : %lf", d1 / d2);
}
