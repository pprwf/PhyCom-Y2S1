    j *= (int)(x + z) / 2; // j = -1440, x = 0.00100000005, z = -81.7529984
    y -= (c + d) / i + x; // y = -39.0009995
    y = x = z - j; // j = 36, x = -117.752998, y = -117.752998, z = -81.7529984

