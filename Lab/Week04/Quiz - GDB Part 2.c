    float x = 0.001, y = 0.00, z = -81.753;
    char c = 'z', d = 'M';

    (d <= 'a') && ('A' >= c-30); // ans = 0
    j * (i - 7) > (y - 2); // ans = 0
    (x * j) >= (z * i); // ans = 1
